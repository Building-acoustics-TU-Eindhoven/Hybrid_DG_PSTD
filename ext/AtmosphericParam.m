classdef AtmosphericParam

    properties
        upstreamFlow % (m/s)
        temperature % (°C)
        temperatureKelvin
        staticPressure % (hPa)
        relativeHumidity % (%)
        airDensity % (kg/m3)
        soundSpeed % (m/s)
        sourcePosition
        receiverPosition
        srDistance
        srHorizontalDistance
    end


    methods

        function obj = AtmosphericParam(configName)

            thisParam = importmeteoparam(configName);

            obj.upstreamFlow = thisParam{1};
            obj.temperature = thisParam{2};
            obj.staticPressure = thisParam{3};
            obj.relativeHumidity = thisParam{4};
            obj.airDensity = thisParam{5};
            obj.temperatureKelvin = 273.15 + obj.temperature;


            % Compute sound speed based on temperature, humidity (and pressure)

            % atmospheric constants
            R0 = 287.058; % gas constant for air
            gamma0 = 1.4003; % specific heat ratio
            A0=6.116441;
            m0=7.591386;
            Tn0=240.7263;
            alpha0 = 28.97/18.02;

            pWS0 = A0*10^(m0*obj.temperature/(obj.temperature+Tn0))*100; % saturation vapor pressure
            e0 = pWS0 * obj.relativeHumidity/100; % partial pressure of water vapor
            C0 = (e0/obj.staticPressure/100) / ...
                 (alpha0*(1-e0/obj.staticPressure/100)); % concentration of water vapor

            % Ostashev's book, Eq. 6.84
            obj.soundSpeed = sqrt(gamma0*R0*obj.temperatureKelvin*(1+0.511*C0));

            % geometry
            [obj.sourcePosition, obj.receiverPosition] = importgeometryparam(configName);
            obj.srDistance = norm(obj.sourcePosition-obj.receiverPosition);
            obj.srHorizontalDistance = abs(obj.sourcePosition(1)-obj.receiverPosition(1));


        end % constructor


        function [time, t_error] = computeTimeOfFlight(obj)
        % Estimate time of flight between source and receiver based on estimated
        % sound speed. Also give error range based on a positioning accuracy
        % of 1 cm, a error of 1% on sound speed + wind convection.

            distance = obj.srDistance;
            time = distance / obj.soundSpeed;

            % uncertainty
            d_error = 1e-2;
            c_error = 0.01*obj.soundSpeed + obj.upstreamFlow;
            t_error = sqrt( (d_error/obj.soundSpeed)^2 + ...
                            (-distance/obj.soundSpeed^2*c_error)^2 );

        end


    end % methods
end % class


